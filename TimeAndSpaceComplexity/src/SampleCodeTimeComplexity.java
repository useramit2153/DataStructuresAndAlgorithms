public class SampleCodeTimeComplexity {
    
    public static void main(String[] args) {
        
        int n = 5;
        int k = 5;
        
        for(int i = 0; i < n; i++){
            for(int j = 1 ; j < k; j++){
                System.out.println(i + j);
            }
        }
    }
    
}
